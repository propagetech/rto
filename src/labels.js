export function regNumberForPdf(rest) {
  const cleaned = String(rest || "")
    .trim()
    .replace(/\s+/g, "-")
    .replace(/-+/g, "-")
    .replace(/^-+|-+$/g, "");
  return cleaned ? `${REG_NUMBER_PREFIX}-${cleaned}` : "";
}

